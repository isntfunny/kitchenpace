    function randomPastDate(monthsBack = 6): Date {
        const now = new Date();
        const daysBack = Math.floor(Math.random() * (monthsBack * 30));
        const date = new Date(now.getTime() - daysBack * 24 * 60 * 60 * 1000);
        return date;
    }

